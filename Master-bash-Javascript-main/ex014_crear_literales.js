// Ejercicio 14: Crear y usar literales de diferentes tipos de dato.

// Literales de número:
// Un número entero
const num1 = 100;

// Un número decimal
const num2 = 3.14;

// Un número en notación científica
const num3 = 1e5; // Equivale a 100000


// Literales de cadena:

// Una cadena de caracteres
const str1 = 'Hola mundo';

// Una cadena de caracteres con comillas dobles
const str2 = "Hola mundo";

// Una cadena de caracteres con caracteres de escape
const str3 = 'Hola, me llamo "Juan"';


// Literales de Booleano:

// Un valor Booleano verdadero
const bool1 = true;

// Un valor Booleano falso
const bool2 = false;



// Literales de objeto:

// Un objeto vacío
const obj1 = {};

// Un objeto con algunas propiedades
const obj2 = {
  nombre: 'Juan',
  apellido: 'Pérez',
  edad: 30
};



// Literales de matriz:

// Una matriz vacía
const arr1 = [];

// Una matriz con algunos elementos
const arr2 = [1, 2, 3];

// Una matriz de objetos
const arr3 = [
  { nombre: 'Juan', apellido: 'Pérez' },
  { nombre: 'Ana', apellido: 'Gómez' },
  { nombre: 'Laura', apellido: 'Rodríguez' },
];

